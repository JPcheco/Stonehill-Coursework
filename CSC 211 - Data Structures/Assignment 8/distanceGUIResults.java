


public class distanceGUIResults
{
   int distance;
   String path;

   public distanceGUIResults()
   {
      path = new String();
   }

   public void setPath( String s )
   {
      path = s;
   }
   
   public String getPath()
   {
      return path;
   }

   public void setDistance( int d )
   {
      distance = d;
   }
   
   public int getDistance()
   {
      return distance;
   }
}